let sumCart = 0;

/* Meals */
document
	.getElementById("btn-add-ChickpeasSalad")
	.addEventListener("click", function () {
		let myStorage = window.localStorage.getItem("sumCart");

		if (!myStorage) {
			window.localStorage.setItem("sumCart", 330);
		} else {
			window.localStorage.setItem("sumCart", parseInt(myStorage) + 330);
		}
	});

